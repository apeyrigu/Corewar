#include "header.h"

int	ft_wlen(wchar_t chr)
{
	if (chr <= 127)
		return (1);
	if (chr <= 2047)
		return (2);
	else if (chr <= 65535)
		return (3);
	else if (chr <= 2097151)
		return (4);
	return (42);
}
