#include "stdlib.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
		{
			if (f != NULL)
				f(&(s[i]));
			i++;
		}
	}
}
