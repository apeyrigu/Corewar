#include "../includes/libft.h"

int		ft_count_if(char **tab, int (*f)(char*))
{
	int c;
	int i;

	c = 0;
	i = 0;
	while (tab[c] != 0 || tab[c] != '\0')
	{
		if (f(tab[c]) == 1)
			i++;
		c++;
	}
	return (i);
}
