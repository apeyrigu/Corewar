#include "../includes/libft.h"

void	ft_putnbr(int n)
{
	long int c;

	c = (long int)n;
	if (c < -2147483648 || c > 2147483647)
		ft_putchar(48);
	if (c < 0)
	{
		ft_putchar('-');
		c *= -1;
	}
	if (c >= 10)
		ft_putnbr(c / 10);
	ft_putchar((c % 10) + 48);
}
