#include "../includes/libft.h"

int		ft_sqrt(int nb)
{
	int i;

	if (nb > 2147483647 || nb < 0)
		return (0);
	i = nb;
	while (nb < (i * i))
		i--;
	if (nb == (i * i))
		return (i);
	if (i * i < nb)
		return (0);
	return (0);
}
